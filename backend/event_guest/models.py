

class Event:
    
    def __init__(self, id, name, category, date, time, city, state, country, venue_name, image_url):
        self.id = id
        self.name = name
        self.category = category
        self.date = date
        self.time = time
        self.city = city
        self.state = state
        self.country = country
        self.venue_name = venue_name
        self.image_url = image_url